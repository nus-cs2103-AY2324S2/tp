package seedu.address.logic.parser;

import static java.util.Objects.requireNonNull;

import java.util.Collection;
import java.util.HashSet;
import java.util.Set;

import seedu.address.commons.core.index.Index;
import seedu.address.commons.util.StringUtil;
import seedu.address.logic.parser.exceptions.ParseException;
import seedu.address.model.person.Address;
import seedu.address.model.person.Birthday;
import seedu.address.model.person.Email;
import seedu.address.model.person.Name;
import seedu.address.model.person.Phone;
import seedu.address.model.person.Priority;
import seedu.address.model.person.SortCriteria;
import seedu.address.model.person.SortOrder;
import seedu.address.model.policy.Policy;
import seedu.address.model.tag.Tag;

/**
 * The type Parser util.
 */
public class ParserUtil {

    public static final String MESSAGE_INVALID_INDEX = "Index is not a non-zero unsigned integer.";

    /**
     * Parses {@code oneBasedIndex} into an {@code Index} and returns it. Leading and trailing whitespaces will be
     * trimmed.
     *
     * @param oneBasedIndex the one based index
     * @return the index
     * @throws ParseException if the specified index is invalid (not non-zero unsigned integer).
     */
    public static Index parseIndex(String oneBasedIndex) throws ParseException {
        String trimmedIndex = oneBasedIndex.trim();
        if (!StringUtil.isNonZeroUnsignedInteger(trimmedIndex)) {
            throw new ParseException(MESSAGE_INVALID_INDEX);
        }
        return Index.fromOneBased(Integer.parseInt(trimmedIndex));
    }

    /**
     * Parses a {@code String name} into a {@code Name}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param name the name
     * @return the name
     * @throws ParseException if the given {@code name} is invalid.
     */
    public static Name parseName(String name) throws ParseException {
        requireNonNull(name);
        String trimmedName = name.trim();
        if (!Name.isValidName(trimmedName)) {
            throw new ParseException(Name.MESSAGE_CONSTRAINTS);
        }
        return new Name(trimmedName);
    }

    /**
     * Parses a {@code String phone} into a {@code Phone}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param phone the phone
     * @return the phone
     * @throws ParseException if the given {@code phone} is invalid.
     */
    public static Phone parsePhone(String phone) throws ParseException {
        requireNonNull(phone);
        String trimmedPhone = phone.trim();
        if (!Phone.isValidPhone(trimmedPhone)) {
            throw new ParseException(Phone.MESSAGE_CONSTRAINTS);
        }
        return new Phone(trimmedPhone);
    }

    /**
     * Parses a {@code String address} into an {@code Address}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param address the address
     * @return the address
     * @throws ParseException if the given {@code address} is invalid.
     */
    public static Address parseAddress(String address) throws ParseException {
        requireNonNull(address);
        String trimmedAddress = address.trim();
        if (!Address.isValidAddress(trimmedAddress)) {
            throw new ParseException(Address.MESSAGE_CONSTRAINTS);
        }
        return new Address(trimmedAddress);
    }

    /**
     * Parses a {@code String birthday} into a {@code Birthday}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param birthday the birthday
     * @return the birthday
     * @throws ParseException if the given {@code birthday} is invalid.
     */
    public static Birthday parseBirthday(String birthday) throws ParseException {
        requireNonNull(birthday);
        String trimmedBirthday = birthday.trim();
        if (!Birthday.isValidBirthday(trimmedBirthday)) {
            throw new ParseException(Birthday.MESSAGE_CONSTRAINTS);
        }
        return new Birthday(trimmedBirthday);
    }

    /**
     * Parses a {@code String priority} into a {@code Priority}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @throws ParseException if the given {@code priority} is invalid.
     */
    public static Priority parsePriority(String priority) throws ParseException {
        requireNonNull(priority);
        String trimmedPriority = priority.trim();
        if (!Priority.isValidPriority(trimmedPriority)) {
            throw new ParseException(Priority.MESSAGE_CONSTRAINTS);
        }
        return new Priority(trimmedPriority);
    }

    /**
     * Parses a {@code String email} into an {@code Email}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param email the email
     * @return the email
     * @throws ParseException if the given {@code email} is invalid.
     */
    public static Email parseEmail(String email) throws ParseException {
        requireNonNull(email);
        String trimmedEmail = email.trim();
        if (!Email.isValidEmail(trimmedEmail)) {
            throw new ParseException(Email.MESSAGE_CONSTRAINTS);
        }
        return new Email(trimmedEmail);
    }

    /**
     * Parses a {@code String tag} into a {@code Tag}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param tag the tag
     * @return the tag
     * @throws ParseException if the given {@code tag} is invalid.
     */
    public static Tag parseTag(String tag) throws ParseException {
        requireNonNull(tag);
        String trimmedTag = tag.trim();
        if (!Tag.isValidTagName(trimmedTag)) {
            throw new ParseException(Tag.MESSAGE_CONSTRAINTS);
        }
        return new Tag(trimmedTag);
    }

    /**
     * Parses {@code Collection<String> tags} into a {@code Set<Tag>}.
     */
    public static Set<Tag> parseTags(Collection<String> tags) throws ParseException {
        requireNonNull(tags);
        final Set<Tag> tagSet = new HashSet<>();
        for (String tagName : tags) {
            tagSet.add(parseTag(tagName));
        }
        return tagSet;
    }

    /**
     * Parses {@code String policyName} and {@code String policyId} into a {@code Policy}.
     * Leading and trailing whitespaces will be trimmed.
     *
     * @param policyName the name
     * @param policyId the id
     * @return the policy
     * @throws ParseException if the given {@code policyName} or {@code policyId} is invalid.
     */
    public static Policy parsePolicyInfo(String policyName, String policyId) throws ParseException {
        requireNonNull(policyName, policyId);
        String trimmedPolicyName = policyName.trim();
        String trimmedPolicyId = policyId.trim();
        if (!Policy.isValidName(trimmedPolicyName)) {
            throw new ParseException(Policy.MESSAGE_CONSTRAINTS_NAME);
        }
        if (!Policy.isValidId(trimmedPolicyId)) {
            throw new ParseException(Policy.MESSAGE_CONSTRAINTS_ID);
        }
        return new Policy(trimmedPolicyName, trimmedPolicyId);
    }

    /**
     * Parses {@code String policyId} into a {@code String}.
     * Leading and trailing whitespaces will be trimmed.
     * @param policyId the policy id
     * @return the policyid
     * @throws ParseException
     */
    public static String parsePolicyId(String policyId) throws ParseException {
        requireNonNull(policyId);
        String trimmedPolicyId = policyId.trim();
        if (!Policy.isValidId(trimmedPolicyId)) {
            throw new ParseException(Policy.MESSAGE_CONSTRAINTS_ID);
        }
        return trimmedPolicyId;
    }

    /**
     * Parses {@code String sortOrder} into a {@code SortOrder}.
     *
     * @param sortOrder the sort order
     * @return the sort order
     * @throws ParseException if the given {@code sortOrder} is invalid
     */
    public static SortOrder parseSortOrder(String sortOrder) throws ParseException {
        requireNonNull(sortOrder);
        String trimmedSortOrder = sortOrder.trim();
        if (!SortOrder.isValidSortOrder(trimmedSortOrder)) {
            throw new ParseException(SortOrder.MESSAGE_CONSTRAINTS);
        }
        return SortOrder.getSortOrder(trimmedSortOrder);
    }

    /**
     * Parses {@code String sortCriteria} into a {@code SortCriteria}.
     *
     * @param sortCriteria the sort criteria
     * @return the sort criteria
     * @throws ParseException if the given {@code sortCriteria} is invalid
     */
    public static SortCriteria parseSortCriteria(String sortCriteria) throws ParseException {
        requireNonNull(sortCriteria);
        String trimmedSortCriteria = sortCriteria.trim();
        if (!SortCriteria.isValidSortCriteria(trimmedSortCriteria)) {
            throw new ParseException(SortCriteria.MESSAGE_CONSTRAINTS);
        }
        return SortCriteria.getSortCriteria(trimmedSortCriteria);
    }
}
